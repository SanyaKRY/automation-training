package collections.thirdmaintask;

public class Car {
	
  String carBrand;
  int carNumbers;
	
  public Car(String carBrand, int carNumbers) {
	super();
	this.carBrand = carBrand;
	this.carNumbers = carNumbers;
  }

  public String getCarBrand() {
	return carBrand;
  }

  public Integer getCarNumbers() {
	return carNumbers;
  }

}
